function rotLeft(a, d)
{
    for(let i = 0; i < d; i++)
    {
        const currentValue = a.shift();
        a.push(currentValue);
    }
    return a;
}

rotLeft([1, 2, 3, 4, 5], 4);
rotLeft([41, 73, 89, 7, 10, 1, 59, 58, 84, 77, 77, 97, 58, 1, 86, 58, 26, 10, 86, 51], 10);
rotLeft([33, 47, 70, 37, 8, 53, 13, 93, 71, 72, 51, 100, 60, 87, 97], 13);

